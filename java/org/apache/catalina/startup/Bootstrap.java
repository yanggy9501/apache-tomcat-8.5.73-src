    /**
     * Main method and entry point when starting Tomcat via the provided
     * scripts.
     *
     * @param args Command line arguments to be processed
     */
    /*
    jvm 参数
    -Dcatalina.home=/xxx/apache-tomcat-8.5.73-src/catalina-home
    -Dcatalina.base=/xxx/apache-tomcat-8.5.73-src/catalina-home
    -Djava.util.logging.manager=org.apache.juli.ClassLoaderLogManager
    -Dfile.encoding=UTF-8
     */
    public static void main(String args[]) {

        synchronized (daemonLock) {
            if (daemon == null) {
                // Don't set daemon until init() has completed
                // 在init（）完成之前不要设置守护进程
                // 创建一个Bootstrap对象
                Bootstrap bootstrap = new Bootstrap();
                try {
                    //调用初始化方法
                    // 初始化ClassLoader:
                    //      ClassLoader commonLoader = null;
                    //      ClassLoader catalinaLoader = null; 绑定到当前线程上
                    //      ClassLoader sharedLoader = null;
                    // catalinaLoader类加载器创建Catalina实例，赋值给catalinaDaemon
                    bootstrap.init(); // 初始化类加载器，创建Catalina
                } catch (Throwable t) {
                    handleThrowable(t);
                    t.printStackTrace();
                    return;
                }
                daemon = bootstrap;
            } else {
                // When running as a service the call to stop will be on a new
                // thread so make sure the correct class loader is used to
                // prevent a range of class not found exceptions.
                Thread.currentThread().setContextClassLoader(daemon.catalinaLoader);
            }
        }

        try {
            String command = "start";
            if (args.length > 0) {
                command = args[args.length - 1];
            }

            if (command.equals("startd")) {
                args[args.length - 1] = "start";
                daemon.load(args);
                daemon.start();
            } else if (command.equals("stopd")) {
                args[args.length - 1] = "stop";
                daemon.stop();
            } else if (command.equals("start")) {
                // 执行这个位置，【让主线程不退出】
                daemon.setAwait(true);
                // 反射调用 catalinaDaemon#load 方法，根据server.xml 创建服务
                daemon.load(args);
                // 反射调用 catalinaDaemon#start 方法，启动服务
                daemon.start();
                if (null == daemon.getServer()) {
                    System.exit(1);
                }
            } else if (command.equals("stop")) {
                daemon.stopServer(args);
            } else if (command.equals("configtest")) {
                daemon.load(args);
                if (null == daemon.getServer()) {
                    System.exit(1);
                }
                System.exit(0);
            } else {
                log.warn("Bootstrap: command \"" + command + "\" does not exist.");
            }
        } catch (Throwable t) {
            // Unwrap the Exception for clearer error reporting
            if (t instanceof InvocationTargetException &&
                    t.getCause() != null) {
                t = t.getCause();
            }
            handleThrowable(t);
            t.printStackTrace();
            System.exit(1);
        }
    }


